// ifelseifelse.cs
using System;

class Program
{
    static void Main()
    {
        int x = 5;
        if (x == 10)
        {
            Console.WriteLine("x is 10");
        }
        else if (x < 10)
        {
            Console.WriteLine("x is less than 10");
        }
        else
        {
            Console.WriteLine("x is greater than 10");
        }
    }
}
